package com.ane56.domain;


public class MoviesFilm {
	 
	 private Long id;
	 private Long filmId;
	 private Long moviesId;
	 private String filmName;
	 private String director;
	 private String toStar;
	 private String releaseTime;
	 private String score;
	 private String moviesName;
	 private String address;
	 private String longitude;
	 private String latitude;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getFilmId() {
		return filmId;
	}
	public void setFilmId(Long filmId) {
		this.filmId = filmId;
	}
	public Long getMoviesId() {
		return moviesId;
	}
	public void setMoviesId(Long moviesId) {
		this.moviesId = moviesId;
	}
	public String getFilmName() {
		return filmName;
	}
	public void setFilmName(String filmName) {
		this.filmName = filmName;
	}
	public String getDirector() {
		return director;
	}
	public void setDirector(String director) {
		this.director = director;
	}
	public String getToStar() {
		return toStar;
	}
	public void setToStar(String toStar) {
		this.toStar = toStar;
	}
	public String getReleaseTime() {
		return releaseTime;
	}
	public void setReleaseTime(String releaseTime) {
		this.releaseTime = releaseTime;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getMoviesName() {
		return moviesName;
	}
	public void setMoviesName(String moviesName) {
		this.moviesName = moviesName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	 
}
